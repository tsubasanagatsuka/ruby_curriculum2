解説
それぞれのメソッドの呼び出し方法についての解説です。

①のメソッドの呼び出し
①は「インスタンスメソッドでもクラスメソッドでも無いメソッド」です。
インスタンスメソッドでもクラスメソッドでも無いメソッドは、メソッド名だけを記述することでメソッドを呼び出せます。引数は無いので()をつけても付けなくても良いです。

メソッドの呼び出し
1
メソッド名()
②のメソッドの呼び出し
②は「クラスメソッド」です。
クラスメソッドは、クラス名.メソッド名()としてメソッドを呼び出します。単にメソッド名だけの記述だと、どのクラスのメソッドか判断できないためです。このときのクラス名は定義のときと同様で1文字目は大文字です。

クラスメソッドの呼び出し
1
クラス名.メソッド名()
③のメソッドの呼び出し
③は「インスタンスメソッド」です。
インスタンスメソッドは、生成した個々のインスタンスが使えるメソッドです。そのため、まずはインスタンスを生成しなければいけません。

