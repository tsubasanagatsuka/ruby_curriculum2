解説
①の理由
①は「インスタンスメソッドでもクラスメソッドでも無いメソッド」です。
インスタンスメソッドやクラスメソッドはクラスの定義内に書かれたメソッドを指します。①のhelloメソッドはHumanクラスの定義の外に書かれているため、クラスとは関係のないメソッドということになります。

②の理由
②は「クラスメソッド」です。
②のメソッドはHumanクラスの定義内にかかれているため、インスタンスメソッドかクラスメソッドのどちらかになります。②のhelloメソッドの定義にはself.が付いています。このself.が付いたメソッドはクラスメソッドです。

