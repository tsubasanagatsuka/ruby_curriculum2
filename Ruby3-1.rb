解説
変数を使うステップは2つです。

変数を定義する（変数名 = 値）
変数を使用する
プログラムは必ず上から下へと順番に処理されます。
そのため変数を使うには、変数を使用する処理よりも上の行で変数を定義しなくてはいけません。

今回の問題文のプログラムでは、1行目にputs "私の名前は#{name}です。"と変数nameを使用していますが、変数nameの定義はその下の3行目にあります。

つまり、定義する前に変数nameを使ってしまっているためエラーが起きました。

正しいソースコードにするには、1行目と3行目を入れ替えて、変数の定義を上に持ってくるだけです。

