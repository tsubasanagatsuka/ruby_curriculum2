模範解答
解答
良い変数名とは、変数の中にどんな値が入っているのか誰が見てもわかる名前のこと。

解説
変数の役割の1つがプログラムを読みやすくすることです。例えば以下のソースコードを見てください。

1
number = 30
これでは変数numberに入っている30という数値が何を指してるかわかりません。しかし、変数名を以下のようにしたらどうでしょうか。

1
age = 30
ageは年齢という意味の英語です。これなら誰が見ても30は年齢を表していることがわかります。

このように変数名の付け方でプログラムのわかり易さが変わります。そのプログラムを初めて見る人でも変数の中身が何かわかるような変数名が良い変数名と言えます。

採点方法
「変数の中身がわかること」が入っていれば5点。それ以外は0点。

